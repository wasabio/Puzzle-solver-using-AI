package miniProject1.utilities;

public class Carre {
		
	public Carre(int number, String letter) {
		this.number = number;
		this.letter = letter;
	}

	private int number;
	private String letter;
	
	public int getNumber() {
		return number;
	}
	
	public void setNumber(int number) {
		this.number = number;
	}
	
	public String getLetter() {
		return letter;
	}
	
	public void setLetter(String letter) {
		this.letter = letter;
	}

}
